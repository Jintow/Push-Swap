#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *str)
{
	int				i;
	unsigned int	nb;
	int				sign;

	nb = 0;
	i = 0;
	sign = 1;
	while (str[i] && (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] && ((str[i] == '-') || str[i] == '+'))
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = str[i] - 48 + (nb * 10);
		i++;
	}
	return (sign * (int)nb);
}
