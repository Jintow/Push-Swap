#include "push_swap.h"

char	**ft_strupcase(char **str)
{
	int	i;
	int	j;

	i = -1;
	while (str[++i])
	{
		j = -1;
		while (str[i][++j])
			if (str[i][j] >= 'a' && str[i][j] <= 'z')
				str[i][j] -= 32;
	}
	return (str);
}
